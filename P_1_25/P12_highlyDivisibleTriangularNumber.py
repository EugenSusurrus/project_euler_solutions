"""
The sequence of triangle numbers is generated by adding the natural numbers.
So the 7th triangle number would be 1 + 2 + 3 + 4 + 5 + 6 + 7 = 28. The first ten terms would be:

1, 3, 6, 10, 15, 21, 28, 36, 45, 55, ...

Let us list the factors of the first seven triangle numbers:

 1: 1
 3: 1,3
 6: 1,2,3,6
10: 1,2,5,10
15: 1,3,5,15
21: 1,3,7,21
28: 1,2,4,7,14,28
We can see that 28 is the first triangle number to have over five divisors.

What is the value of the first triangle number to have over five hundred divisors?
"""
def divisors(n):
    divs = [1]
    for i in range(2, int(n**(1/2)) + 1):
        if n%i == 0:
            divs.extend([i, int(n/i)])
    divs.extend([n])
    return list(set(divs))


def highlyDivisibleTriangularNumber(targetDiv):
    numDiv = 1
    n = 1       #nth triangular number
    triangularNumber = 0
    while numDiv < targetDiv:
        triangularNumber = int(n*(n+1)/2)
        divs = [1]
        for i in range(2, int(triangularNumber ** (1 / 2)) + 1):
            if triangularNumber % i == 0:
                divs.extend([i, int(triangularNumber / i)])
        divs.extend([triangularNumber])
        numDiv = len(list(set(divs)))
        n += 1

    return triangularNumber


print(highlyDivisibleTriangularNumber(500))